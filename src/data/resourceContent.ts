// Fonction pour télécharger un article en .txt
export function downloadArticle(title: string, description: string, content: string) {
  const fullContent = `${title}\n\n${description}\n\n${content.replace(/<[^>]*>/g, '\n').replace(/\n\n+/g, '\n\n').trim()}`;
  
  const blob = new Blob([fullContent], { type: 'text/plain;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `${title.replace(/[^a-z0-9]/gi, '_').toLowerCase()}.txt`;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}